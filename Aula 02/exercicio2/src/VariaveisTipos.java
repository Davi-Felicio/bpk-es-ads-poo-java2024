public class VariaveisTipos {
    public static void main(String[] args) {
        // Declaração e inicialização de variáveis de diferentes tipos

        // Tipo inteiro
        int numeroInteiro = 42;

        // Tipo double (número decimal)
        double numeroDecimal = 3.14;

        // Tipo boolean (verdadeiro ou falso)
        boolean verdadeiroOuFalso = true;

        // Tipo char (caractere único)
        char caractere = 'A';

        // Impressão dos valores no console
        System.out.println("Valor de int: " + numeroInteiro);
        System.out.println("Valor de double: " + numeroDecimal);
        System.out.println("Valor de boolean: " + verdadeiroOuFalso);
        System.out.println("Valor de char: " + caractere);
    }
}